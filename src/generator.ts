// Helper to clean and detect domain of user input
function detectDomain(keyword: string): string {
  const kw = keyword.toLowerCase();
  if (kw.includes('알고리즘') || kw.includes('algorithm') || kw.includes('seo') || kw.includes('노출') || kw.includes('추천')) {
    return 'algorithm';
  }
  if (kw.includes('시니어') || kw.includes('senior') || kw.includes('사연') || kw.includes('나이') || kw.includes('실버')) {
    return 'senior';
  }
  if (kw.includes('도구') || kw.includes('aitools') || kw.includes('목소리') || kw.includes('tts') || kw.includes('자동')) {
    return 'aitools';
  }
  if (kw.includes('수익') || kw.includes('돈') || kw.includes('애드센스') || kw.includes('광고') || kw.includes('중방') || kw.includes('협찬')) {
    return 'monetization';
  }
  if (kw.includes('초보') || kw.includes('시작') || kw.includes('왕초보') || kw.includes('beginner') || kw.includes('세팅')) {
    return 'beginner';
  }
  if (kw.includes('공부') || kw.includes('영어') || kw.includes('학습') || kw.includes('회화')) {
    return 'education';
  }
  if (kw.includes('요리') || kw.includes('음식') || kw.includes('레시피') || kw.includes('김치찌개') || kw.includes('맛있는')) {
    return 'cooking';
  }
  if (kw.includes('운동') || kw.includes('헬스') || kw.includes('다이어트') || kw.includes('피트니스')) {
    return 'fitness';
  }
  return 'general';
}

